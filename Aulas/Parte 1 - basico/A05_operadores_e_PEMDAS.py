print(10 + 10)  # adição
print(10 - 10)  # subtração
print(10 * 10)  # multiplicação
print(10 / 10)  # divisão
print(10 // 10)  # divisão inteira
print(10 % 4)  # resto da divisão
print(10 ** 10)  # exponenciação

print(10 * 'Ovo')
print('10' + 'Ovo')
print('Michael' + ' ' + 'Jackson tem ' + str(100) + ' anos.')

print(5 + 2 * 10)
print((5 + 2) * 10)

# ordem de precedência = PEMDAS
#
# P = parênteses
# E = exponentes
# MD = multiplicação ou divisão
# AS = adição ou subtração
